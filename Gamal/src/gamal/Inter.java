    private void generateMsgBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateMsgBtnActionPerformed
        if (secretKeyTextField.getText()!= "" && messageTextArea.getText() != ""){
            ecMsg="";
            ecMessage.clear();
            try {
                secretKey = new BigInteger(secretKeyTextField.getText());
                c = b.modPow(secretKey, p);
                // Se toma el mensaje a cifrar
                String s = messageTextArea.getText();
                BigInteger r = new BigInteger(64, sc);
                byte[] bytes = s.getBytes("UTF-8");
                for( byte elem: bytes ){
                    int aux = elem;
                    String value = Integer.toString(aux);
                    BigInteger X = new BigInteger(value);
                    BigInteger EC = X.multiply(c.modPow(r, p)).mod(p);
                    ecMessage.add(EC);
                    ecMsg += EC.toString() + "\n";
                }
                ecMessageField.setText(ecMsg);
                brmodp = b.modPow(r, p);
            } catch (UnsupportedEncodingException ex) {
                Logger.getLogger(Inter.class.getName()).log(Level.SEVERE, null, ex);
            }
        }        
    }//GEN-LAST:event_generateMsgBtnActionPerformed

    private void descifrarMessageBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_descifrarMessageBtnActionPerformed
       
        if(!ecMessage.isEmpty()){
            dcMsg="";
            BigInteger crmodp = brmodp.modPow(secretKey, p);
            BigInteger d = crmodp.modInverse(p);
            List<BigInteger> dcMessage = new ArrayList<>();
            // descifrar cada byte cifrado
            for (BigInteger elem: ecMessage){
                BigInteger ad = d.multiply(elem).mod(p);
                dcMessage.add(ad);
            }
            // interpretar el valor ascii en caracteres
            for (BigInteger elem: dcMessage){
                int aux = Integer.parseInt(elem.toString());
                dcMsg += (char)aux;
            }
            dcMessageField.setText(dcMsg);
        }
    }//GEN-LAST:event_descifrarMessageBtnActionPerformed

